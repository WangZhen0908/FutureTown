package com.vvage.futuretown.model;

public class Image {
	private int im;

	public Image(int im) {
		super();
		this.im = im;
	}

	public int getIm() {
		return im;
	}

	public void setIm(int im) {
		this.im = im;
	}

	@Override
	public String toString() {
		return "Image [im=" + im + "]";
	}
	

}
